#include <gtest/gtest.h>
#include <lisle/create>

using namespace lisle;

static void worker ()
{}

TEST (createTest, simple)
{
	// Ensure starting a thread works
	ASSERT_NO_THROW(
		{
		Thread thread(&worker);
		Thrid tid = create(thread);
		tid.join();
		}
	);
}
